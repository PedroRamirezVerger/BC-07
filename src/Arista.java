
public class Arista {
	private int nodoI;
	private int nodoF;
	private String calle;
	private int longitud;
	
	public int getNodoI() {
		return nodoI;
	}
	public void setNodoI(int nodoI) {
		this.nodoI = nodoI;
	}
	public int getNodoF() {
		return nodoF;
	}
	public void setNodoF(int nodoF) {
		this.nodoF = nodoF;
	}
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public int getLongitud() {
		return longitud;
	}
	public void setLongitud(int longitud) {
		this.longitud = longitud;
	}
	
	
}
